public class JavaTest05 {

	public static void main(String[] args) {
		
		// 초기선언
		int num = 0;
		int result;
		
		result = num++;
		System.out.println("result : " + result);
		
		result = ++num;
		System.out.println("result : " + result);
		
		result = num--;
		System.out.println("result : " + result);
		
		result = --num;
		System.out.println("result : " + result);
		
		
	}
}
